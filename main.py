# Operations Mathematic
print(1 + 3) # Addition: 4
print(3 - 2) # Soustraction: -1
print(4 * 5) # Multiplication: 20
print(5 / 2) # Division: 2.5 (retourne un float !)
print(2 ** 2)# Puissance: 4
print(5 % 4)  # Modulo: 1 (reste de la division de 5 par 4)
print(4 // 1)  # D 2 (5 ÷ 2 = 2.5, arrondi vers le bas à 2)
print(7 // 3)  # 2 (7 ÷ 3 = 2.33, arrondi vers le bas à 2)

# Types
print(type(1 + 3))  # <class 'int'>
print(type(3 - 2))  # <class 'int'>
print(type(4 * 5))  # <class 'int'>
print(type(2 / 3))  # <class 'float'>
print(type(8 / 4))  # <class 'float'> - La division retourne toujours un float !

print(type(20 + 1.1))  # <class 'float'>
print(20 + 1.1)  # 21.1
print(type(9.9 + 1.1))  # <class 'float'>
print(9.9 + 1.1)  # 11.0 (c'est toujours un float !)


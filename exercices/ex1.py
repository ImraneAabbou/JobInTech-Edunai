# Un étudiant achète 25 cahiers puis en achète 17 de plus. Combien de cahiers au total ?
# Un magasin a 50 articles et en vend 23. Combien d'articles restent-ils ?
# Si une boîte contient 8 articles, combien d'articles y a-t-il dans 6 boîtes ?
# 45 étudiants doivent être répartis équitablement en 9 groupes. Combien d'étudiants par groupe ?

print(25 + 17) # total des cahiers
print(50 - 23) # nouveau total des articles
print(8 * 6) # nbr d'articles dans 6 boites
print(45 / 9) # nbr d'etudiant par groupe

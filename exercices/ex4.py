# 17 personnes doivent s'asseoir à des tables de 5. Utilisez // et % pour trouver combien de tables complètes et combien de personnes restantes
# 23 bonbons partagés équitablement entre 4 enfants. Utilisez // et % pour trouver combien de bonbons par enfant et combien en reste-t-il
# 30 livres arrangés en rangées de 7. Utilisez // et % pour trouver combien de rangées complètes et combien de livres dans la rangée incomplète


print(17 // 5) # tables completes
print(17 % 5) # personnes restantes

print(23 // 4) # bonbons partages
print(23 % 4) # bonbons rest

print(30 // 7) # nbr rangees completes
print(30 % 7) # nbr livres dans la rangee incomplete
